package capitolul_2;

public class Exemplul_2 {
    public static void main(String[] args) {
        System.out.println("Rezultatul calcului este: " + 9/3); // 3
        System.out.println("Rezultatul calcului este: " + 9 % 3); // 0

        System.out.println("Rezultatul calcului este: " + 10/3); // 3
        System.out.println("Rezultatul calcului este: " + 10 % 3); // 1

        System.out.println("Rezultatul calcului este: " + 11/3); // 3
        System.out.println("Rezultatul calcului este: " + 11 % 3); // 2

        System.out.println("Rezultatul calcului este: " + 12/3); // 4
        System.out.println("Rezultatul calcului este: " + 12 % 3); // 0
    }
}
